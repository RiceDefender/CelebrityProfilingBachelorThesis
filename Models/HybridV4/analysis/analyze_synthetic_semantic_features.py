def top_examples(rows: List[Dict[str, Any]], task: str) -> Dict[str, Dict[str, List[Dict[str, Any]]]]:
    if task == "gender":
        groups_features = {
            "female_to_male": ["relationship_dominance_index", "female_uncertainty_pressure", "female_context_pressure"],
            "male_to_female": ["relationship_dominance_index", "female_uncertainty_pressure", "low_relationship_male_pressure"],
        }
    else:
        groups_features = {
            "creator_to_performer": ["creator_recovery_pressure", "business_creator_support", "entertainer_pressure_index"],
            "creator_to_politics": ["politics_recovery_pressure", "politics_dominance_index"],
            "creator_to_sports": ["sports_recovery_pressure", "sports_dominance_index"],
            "sports_to_performer": ["sports_recovery_pressure", "sports_dominance_index", "entertainer_pressure_index"],
            "politics_to_creator": ["politics_recovery_pressure", "politics_dominance_index"],
        }
    out: Dict[str, Dict[str, List[Dict[str, Any]]]] = {}
    for group, feats in groups_features.items():
        members = [r for r in rows if r["group"] == group]
        if not members:
            continue
        out[group] = {}
        for feat in feats:
            reverse = not feat.startswith("creator_recovery") and feat != "business_creator_support"
            # For creator recovery, high positive means more creator-like; for entertainer index high means performer-like.
            if feat in {"creator_recovery_pressure", "business_creator_support"}:
                reverse = True
            ranked = sorted(members, key=lambda r: safe_float(r["synthetic_features"].get(feat)), reverse=reverse)[:10]
            out[group][feat] = [compact_example(r, [feat]) for r in ranked]
    return out


def compact_hits(row: Dict[str, Any], limit: int = 8) -> str:
    hits = row.get("top_keyword_hits") or []
    parts: List[str] = []
    for item in hits[:limit]:
        if isinstance(item, (list, tuple)) and len(item) >= 2:
            parts.append(f"{item[0]}:{item[1]}")
    return ", ".join(parts)


