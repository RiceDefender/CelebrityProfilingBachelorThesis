def safe_float(value: Any, default: float = 0.0) -> float:
    try:
        if value is None:
            return default
        return float(value)
    except (TypeError, ValueError):
        return default


def ratio(a: float, b: float) -> float:
    return a / (b + EPS)


def clip(value: float, lo: float, hi: float) -> float:
    return max(lo, min(hi, value))


def log1p_pos(value: float) -> float:
    return math.log1p(max(0.0, value))


def sigmoid_like(value: float) -> float:
    # Monotonic bounded transform; useful for very spiky ratio scores.
    return value / (1.0 + abs(value)) if value != 0 else 0.0


def mean(values: Sequence[float]) -> float:
    return sum(values) / len(values) if values else 0.0


def stdev(values: Sequence[float]) -> float:
    if len(values) < 2:
        return 0.0
    return statistics.pstdev(values)


def cohen_d(a: Sequence[float], b: Sequence[float]) -> float:
    if not a or not b:
        return 0.0
    ma, mb = mean(a), mean(b)
    va = statistics.pvariance(a) if len(a) > 1 else 0.0
    vb = statistics.pvariance(b) if len(b) > 1 else 0.0
    pooled = math.sqrt((va + vb) / 2.0)
    return 0.0 if pooled <= EPS else (ma - mb) / pooled


def macro_f1(y_true: Sequence[str], y_pred: Sequence[str]) -> float:
    labels = sorted(set(y_true) | set(y_pred))
    if not labels:
        return 0.0
    f1s: List[float] = []
    for label in labels:
        tp = sum(1 for t, p in zip(y_true, y_pred) if t == label and p == label)
        fp = sum(1 for t, p in zip(y_true, y_pred) if t != label and p == label)
        fn = sum(1 for t, p in zip(y_true, y_pred) if t == label and p != label)
        precision = tp / (tp + fp) if (tp + fp) else 0.0
        recall = tp / (tp + fn) if (tp + fn) else 0.0
        f1 = (2 * precision * recall / (precision + recall)) if (precision + recall) else 0.0
        f1s.append(f1)
    return mean(f1s)


def accuracy(y_true: Sequence[str], y_pred: Sequence[str]) -> float:
    return sum(1 for t, p in zip(y_true, y_pred) if t == p) / len(y_true) if y_true else 0.0


def load_json(path: Path) -> Dict[str, Any]:
    with path.open("r", encoding="utf-8") as f:
        return json.load(f)


def flatten_top_examples(report: Dict[str, Any]) -> List[Dict[str, Any]]:
    rows: List[Dict[str, Any]] = []
    seen: set[str] = set()
    for examples in report.get("top_examples", {}).values():
        if not isinstance(examples, list):
            continue
        for row in examples:
            cid = str(row.get("celebrity_id", ""))
            if cid and cid not in seen:
                rows.append(row)
                seen.add(cid)
    return rows


def load_profiles(task: str) -> Tuple[List[Dict[str, Any]], Path, str]:
    path = SEMANTIC_DIR / f"{task}_semantic_signal_report.json"
    report = load_json(path)
    rows = report.get("profiles")
    source = "profiles"
    if not isinstance(rows, list) or not rows:
        rows = report.get("profile_rows")
        source = "profile_rows"
    if not isinstance(rows, list) or not rows:
        rows = flatten_top_examples(report)
        source = "top_examples_fallback"
    if not rows:
        raise ValueError(
            f"No profile rows found in {path}. Re-run semantic signal analysis with full profiles output."
        )
    clean_rows: List[Dict[str, Any]] = []
    for row in rows:
        if not isinstance(row, dict):
            continue
        true_label = row.get("true_label") or row.get("true") or row.get("gold")
        pred_label = row.get("pred_label") or row.get("prediction") or row.get("pred")
        cid = row.get("celebrity_id") or row.get("id") or row.get("profile_id")
        if true_label is None or pred_label is None or cid is None:
            continue
        row = dict(row)
        row["celebrity_id"] = str(cid)
        row["true_label"] = str(true_label)
        row["pred_label"] = str(pred_label)
        row["group"] = row.get("group") or make_group(row["true_label"], row["pred_label"])
        row.setdefault("signal_values", {})
        row.setdefault("top_keyword_hits", [])
        clean_rows.append(row)
    return clean_rows, path, source


def make_group(true_label: str, pred_label: str) -> str:
    if true_label == pred_label:
        return f"correct_{true_label}"
    return f"{true_label}_to_{pred_label}"


def signal(row: Dict[str, Any], name: str) -> float:
    return safe_float(row.get("signal_values", {}).get(name, 0.0))


def add_synthetic_features(task: str, row: Dict[str, Any]) -> Dict[str, float]:
    if task == "gender":
        family = signal(row, "family_signal")
        relationship = signal(row, "relationship_signal")
        female_role = signal(row, "female_role_signal")
        violence = signal(row, "violence_against_women_signal")
        social = signal(row, "emotion_social_issue_signal")
        male_role = signal(row, "male_role_signal")

        female_context = family + relationship + female_role + violence
        social_context = violence + social
        role_balance = female_role - male_role
        relationship_balance = relationship - male_role
        family_balance = family - male_role

        features = {
            # Existing strong ratios / differences.
            "relationship_minus_male_role": relationship_balance,
            "female_minus_male_role": role_balance,
            "family_minus_male_role": family_balance,
            "female_context_minus_male_role": female_context - male_role,
            "violence_social_combo": social_context,
            # New synthetic higher-order features.
            "relationship_dominance_index": relationship_balance / (relationship + male_role + 1.0),
            "female_role_dominance_index": role_balance / (female_role + male_role + 1.0),
            "female_context_density": female_context / (female_context + male_role + social + 1.0),
            "relationship_share_of_context": relationship / (female_context + male_role + social + 1.0),
            "female_uncertainty_pressure": relationship_balance + 0.5 * role_balance + 0.25 * family_balance,
            "female_context_pressure": relationship_balance + role_balance + family_balance + 0.5 * social_context,
            "bounded_relationship_pressure": sigmoid_like(relationship_balance),
            "log_relationship_advantage": log1p_pos(relationship_balance),
            "low_relationship_male_pressure": male_role - relationship,
        }
        return features

    if task == "occupation":
        creator = signal(row, "creator_signal")
        performer = signal(row, "performer_signal")
        sports = signal(row, "sports_signal")
        politics = signal(row, "politics_signal")
        business = signal(row, "business_branding_signal")
        fan = signal(row, "fan_community_signal")

        performer_adv = performer - creator
        fan_adv = fan - creator
        sports_adv = sports - performer
        politics_adv = politics - creator
        creator_adv = creator - max(performer, sports, politics, fan)
        entertainer_pressure = performer + fan - creator
        politics_pressure = politics + 0.25 * fan - creator
        sports_pressure = sports - 0.5 * performer + 0.25 * fan - creator
        creator_recovery_pressure = creator - performer - fan

        features = {
            # Existing ratios / differences.
            "performer_minus_creator": performer_adv,
            "fan_minus_creator": fan_adv,
            "performer_fan_minus_creator": entertainer_pressure,
            "sports_minus_performer": sports_adv,
            "politics_minus_creator": politics_adv,
            # New synthetic higher-order features.
            "performer_dominance_index": performer_adv / (performer + creator + 1.0),
            "fan_dominance_index": fan_adv / (fan + creator + 1.0),
            "entertainer_pressure_index": entertainer_pressure / (performer + fan + creator + 1.0),
            "politics_dominance_index": politics_adv / (politics + creator + 1.0),
            "sports_dominance_index": sports_adv / (sports + performer + 1.0),
            "creator_residual_strength": creator_adv,
            "creator_recovery_pressure": creator_recovery_pressure,
            "politics_recovery_pressure": politics_pressure,
            "sports_recovery_pressure": sports_pressure,
            "bounded_politics_pressure": sigmoid_like(politics_adv),
            "bounded_sports_pressure": sigmoid_like(sports_adv),
            "bounded_creator_recovery": sigmoid_like(creator_recovery_pressure),
            "business_creator_support": business + creator - performer,
        }
        return features

    raise ValueError(f"Unsupported task: {task}")


