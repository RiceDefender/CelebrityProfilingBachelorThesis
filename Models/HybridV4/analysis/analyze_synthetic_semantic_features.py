class Rule:
    def __init__(self, name: str, description: str, from_label: str, to_label: str, condition: Callable[[Dict[str, Any]], bool]):
        self.name = name
        self.description = description
        self.from_label = from_label
        self.to_label = to_label
        self.condition = condition


