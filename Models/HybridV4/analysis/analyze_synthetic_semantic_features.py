def enrich_rows(task: str, rows: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    out: List[Dict[str, Any]] = []
    for row in rows:
        row = dict(row)
        row["synthetic_features"] = add_synthetic_features(task, row)
        row["group"] = make_group(row["true_label"], row["pred_label"])
        out.append(row)
    return out


def group_stats(rows: List[Dict[str, Any]], feature_names: Sequence[str]) -> Dict[str, Dict[str, float]]:
    grouped: Dict[str, List[Dict[str, Any]]] = defaultdict(list)
    for row in rows:
        grouped[row["group"]].append(row)
    stats: Dict[str, Dict[str, float]] = {}
    for group, members in sorted(grouped.items()):
        item: Dict[str, float] = {"n": float(len(members))}
        for feat in feature_names:
            item[feat] = mean([safe_float(m["synthetic_features"].get(feat)) for m in members])
        stats[group] = item
    return stats


def compare_groups(rows: List[Dict[str, Any]], feature_names: Sequence[str], comparisons: Sequence[Tuple[str, str]]) -> Dict[str, List[Dict[str, float]]]:
    grouped: Dict[str, List[Dict[str, Any]]] = defaultdict(list)
    for row in rows:
        grouped[row["group"]].append(row)
    out: Dict[str, List[Dict[str, float]]] = {}
    for a, b in comparisons:
        if a not in grouped or b not in grouped:
            continue
        entries = []
        for feat in feature_names:
            av = [safe_float(r["synthetic_features"].get(feat)) for r in grouped[a]]
            bv = [safe_float(r["synthetic_features"].get(feat)) for r in grouped[b]]
            entries.append({
                "feature": feat,
                "mean_a": mean(av),
                "mean_b": mean(bv),
                "diff": mean(av) - mean(bv),
                "cohen_d": cohen_d(av, bv),
            })
        entries.sort(key=lambda x: abs(x["cohen_d"]), reverse=True)
        out[f"{a} vs {b}"] = entries
    return out


