def simulate_rule(rows: List[Dict[str, Any]], task: str, rule: Dict[str, str]) -> Dict[str, Any]:
    before_rows = base_rows(rows)
    after_rows = base_rows(rows)
    changed_examples = []
    target = new_label_for_rule(task, rule["name"])

    changed = improved = harmed = still_wrong = 0
    local_before_true = []
    local_before_pred = []
    local_after_pred = []

    for row, out_row in zip(rows, after_rows):
        if not rule_applies(row, task, rule["name"]):
            continue

        old = pred_label(row)
        gold = true_label(row)
        if old == target:
            continue

        changed += 1
        before_correct = old == gold
        after_correct = target == gold
        if after_correct and not before_correct:
            improved += 1
        elif before_correct and not after_correct:
            harmed += 1
        elif not after_correct:
            still_wrong += 1

        out_row["_sim_pred"] = target
        local_before_true.append(gold)
        local_before_pred.append(old)
        local_after_pred.append(target)

        ex = deepcopy(row)
        ex["old_pred"] = old
        ex["new_pred"] = target
        ex["improved"] = after_correct and not before_correct
        ex["harmed"] = before_correct and not after_correct
        changed_examples.append(ex)

    global_before = metrics(before_rows)
    global_after = metrics(after_rows)

    local_before_acc = (
        sum(1 for t, p in zip(local_before_true, local_before_pred) if t == p) / changed
        if changed else 0.0
    )
    local_after_acc = (
        sum(1 for t, p in zip(local_before_true, local_after_pred) if t == p) / changed
        if changed else 0.0
    )

    return {
        "rule": rule,
        "changed": changed,
        "improved": improved,
        "harmed": harmed,
        "still_wrong": still_wrong,
        "global_before": global_before,
        "global_after": global_after,
        "global_delta_accuracy": global_after["accuracy"] - global_before["accuracy"],
        "global_delta_macro_f1": global_after["macro_f1"] - global_before["macro_f1"],
        "local_before_accuracy": local_before_acc,
        "local_after_accuracy": local_after_acc,
        "changed_examples": changed_examples,
    }


def format_ratio_snippet(row: Dict[str, Any], task: str) -> str:
    rf = row.get("ratio_features", {})
    if task == "gender":
        keys = [
            "female_minus_male_role",
            "relationship_minus_male_role",
            "family_minus_male_role",
            "female_context_minus_male_role",
        ]
    else:
        keys = [
            "performer_minus_creator",
            "creator_minus_performer",
            "fan_minus_creator",
            "performer_fan_minus_creator",
            "politics_minus_creator",
            "sports_minus_performer",
        ]
    return ", ".join(f"{k}={rf.get(k, 0.0):.2f}" for k in keys if k in rf)


def write_markdown(task: str, source_path: str, row_source: str, rows: List[Dict[str, Any]], results: List[Dict[str, Any]], out_path: str) -> None:
    baseline = metrics(base_rows(rows))
    with open(out_path, "w", encoding="utf-8") as f:
        f.write(f"# Semantic Correction Rule Simulation: {task}\n\n")
        f.write("> This is an error-analysis simulation on existing predictions. It should not be reported as a final tuned test result.\n\n")
        f.write(f"Script version: `{SCRIPT_VERSION}`\n\n")
        f.write(f"Source semantic report: `{source_path}`\n\n")
        f.write(f"Profile row source: `{row_source}`\n\n")
        if row_source.endswith("partial"):
            f.write("> WARNING: The report did not contain full profile rows. Metrics below are partial and not global. Regenerate semantic signal reports with full `profiles`.\n\n")

        f.write("## Global baseline on all loaded profiles\n\n")
        f.write("| n | accuracy | macro_f1 |\n")
        f.write("| ---: | ---: | ---: |\n")
        f.write(f"| {baseline['n']} | {baseline['accuracy']:.4f} | {baseline['macro_f1']:.4f} |\n\n")

        f.write("## Rule results\n\n")
        f.write("| rule | change | conditions | changed | improved | harmed | still wrong | global Δacc | global Δmacro-F1 | global after acc | global after macro-F1 | local before acc | local after acc |\n")
        f.write("| --- | --- | --- | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: |\n")
        for res in results:
            rule = res["rule"]
            f.write(
                f"| `{rule['name']}` | {rule['change']} | `{rule['conditions']}` "
                f"| {res['changed']} | {res['improved']} | {res['harmed']} | {res['still_wrong']} "
                f"| {res['global_delta_accuracy']:+.4f} | {res['global_delta_macro_f1']:+.4f} "
                f"| {res['global_after']['accuracy']:.4f} | {res['global_after']['macro_f1']:.4f} "
                f"| {res['local_before_accuracy']:.4f} | {res['local_after_accuracy']:.4f} |\n"
            )

        f.write("\n## Changed examples by rule\n\n")
        for res in results:
            rule = res["rule"]
            f.write(f"### `{rule['name']}`\n\n")
            f.write(f"{rule['description']}\n\n")
            examples = res["changed_examples"]
            if not examples:
                f.write("No changed profiles.\n\n")
                continue
            f.write("| id | true | old pred | new pred | improved | harmed | tokens | key ratios | top keyword hits |\n")
            f.write("| --- | --- | --- | --- | ---: | ---: | ---: | --- | --- |\n")
            for ex in examples[:30]:
                f.write(
                    f"| {profile_id(ex)} | {true_label(ex)} | {ex.get('old_pred', '')} | {ex.get('new_pred', '')} "
                    f"| {ex.get('improved')} | {ex.get('harmed')} | {token_count(ex)} "
                    f"| {format_ratio_snippet(ex, task)} | {top_hits(ex)} |\n"
                )
            f.write("\n")

        f.write("## Interpretation note\n\n")
        if task == "gender":
            f.write("For gender, rules are mainly explanatory. Relationship and female-context ratios can flag uncertain cases, but they should not be treated as reliable correction rules without a separate validation setup.\n")
        else:
            f.write("For occupation, rules should be interpreted as diagnostic probes. A positive delta suggests that the semantic feature direction matches some real errors, but the rule may still overfit the analyzed test split.\n")


def analyze_task(task: str) -> Dict[str, Any]:
    source_path = os.path.join(INPUT_DIR, TASK_FILES[task])
    report = read_json(source_path)
    rows_raw, row_source = load_profile_rows(report)
    rows = [add_ratio_features(r, task) for r in rows_raw if true_label(r) and pred_label(r)]

    print(f"[INFO] {task}: loaded profile rows = {len(rows)} from {row_source}")
    if len(rows) != 400:
        print(
            f"[WARN] {task}: expected 400 rows for PAN test, got {len(rows)}. "
            "If this is not intended, regenerate semantic signal reports with full profile rows."
        )

    results = [simulate_rule(rows, task, rule) for rule in rules_for_task(task)]

    ensure_dir(OUTPUT_DIR)
    md_path = os.path.join(OUTPUT_DIR, f"{task}_semantic_rule_simulation.md")
    json_path = os.path.join(OUTPUT_DIR, f"{task}_semantic_rule_simulation.json")

    write_markdown(task, source_path, row_source, rows, results, md_path)

    serializable = {
        "script_version": SCRIPT_VERSION,
        "task": task,
        "source_path": source_path,
        "row_source": row_source,
        "n_rows": len(rows),
        "global_baseline": metrics(base_rows(rows)),
        "results": results,
    }
    with open(json_path, "w", encoding="utf-8") as f:
        json.dump(serializable, f, ensure_ascii=False, indent=2)

    print(f"[OK] Saved {md_path}")
    print(f"[OK] Saved {json_path}")
    return serializable


def write_summary(all_results: Dict[str, Dict[str, Any]]) -> None:
    path = os.path.join(OUTPUT_DIR, "semantic_rule_simulation_summary.md")
    with open(path, "w", encoding="utf-8") as f:
        f.write("# Semantic Rule Simulation Summary\n\n")
        f.write(f"Script version: `{SCRIPT_VERSION}`\n\n")
        f.write("> Diagnostic post-hoc simulation on existing HybridV4 predictions. Do not report this as a final tuned test result.\n\n")
        for task, data in all_results.items():
            base = data["global_baseline"]
            f.write(f"## {task}\n\n")
            f.write(f"Rows loaded: **{data['n_rows']}** from `{data['row_source']}`\n\n")
            f.write(f"Global baseline accuracy: **{base['accuracy']:.4f}**, macro-F1: **{base['macro_f1']:.4f}**.\n\n")
            f.write("| rule | changed | improved | harmed | global Δaccuracy | global Δmacro-F1 | local before acc | local after acc |\n")
            f.write("| --- | ---: | ---: | ---: | ---: | ---: | ---: | ---: |\n")
            for res in data["results"]:
                f.write(
                    f"| `{res['rule']['name']}` | {res['changed']} | {res['improved']} | {res['harmed']} "
                    f"| {res['global_delta_accuracy']:+.4f} | {res['global_delta_macro_f1']:+.4f} "
                    f"| {res['local_before_accuracy']:.4f} | {res['local_after_accuracy']:.4f} |\n"
                )
            f.write("\n")
    print(f"[OK] Saved {path}")


def main() -> None:
    parser = argparse.ArgumentParser(description="Simulate semantic correction rules for HybridV4 error analysis.")
    parser.add_argument("--task", choices=["gender", "occupation", "all"], default="all")
    args = parser.parse_args()

    tasks = ["gender", "occupation"] if args.task == "all" else [args.task]
    all_results = {task: analyze_task(task) for task in tasks}
    if len(all_results) > 1:
        write_summary(all_results)


if __name__ == "__main__":
    main()
