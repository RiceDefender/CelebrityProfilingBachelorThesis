def read_json(path: str) -> Any:
    with open(path, "r", encoding="utf-8") as f:
        return json.load(f)


def ensure_dir(path: str) -> None:
    os.makedirs(path, exist_ok=True)


def safe_float(x: Any, default: float = 0.0) -> float:
    try:
        if x is None:
            return default
        return float(x)
    except (TypeError, ValueError):
        return default


def profile_id(row: Dict[str, Any]) -> str:
    for key in ["celebrity_id", "id", "profile_id"]:
        if key in row:
            return str(row[key])
    return ""


def true_label(row: Dict[str, Any]) -> str:
    for key in ["true_label", "gold_label", "label", "true"]:
        if key in row:
            return str(row[key])
    return ""


def pred_label(row: Dict[str, Any]) -> str:
    for key in ["pred_label", "fusion_pred_label", "prediction", "predicted_label", "pred"]:
        if key in row:
            return str(row[key])
    return ""


def token_count(row: Dict[str, Any]) -> int:
    for key in ["token_count", "tokens", "n_tokens"]:
        if key in row:
            try:
                return int(row[key])
            except (TypeError, ValueError):
                pass
    return 0


def signal_values(row: Dict[str, Any]) -> Dict[str, float]:
    values = row.get("signal_values", {})
    if not isinstance(values, dict):
        values = {}
    return {k: safe_float(v) for k, v in values.items()}


def top_hits(row: Dict[str, Any], max_items: int = 8) -> str:
    hits = row.get("top_keyword_hits", [])
    out = []
    if isinstance(hits, list):
        for item in hits[:max_items]:
            if isinstance(item, (list, tuple)) and len(item) >= 2:
                out.append(f"{item[0]}:{item[1]}")
            elif isinstance(item, dict):
                token = item.get("token") or item.get("keyword")
                count = item.get("count") or item.get("value")
                if token is not None and count is not None:
                    out.append(f"{token}:{count}")
    return ", ".join(out)


def load_profile_rows(report: Dict[str, Any]) -> Tuple[List[Dict[str, Any]], str]:
    """
    Load full profile rows from a semantic signal report.

    v2 intentionally avoids using top_examples as the primary source because that
    produces partial baselines such as n=40 or n=79. Full reports generated by
    analyze_semantic_error_signals.py should contain one of the full-row keys
    below. If an older report lacks them, we fall back to top_examples and mark
    the source explicitly in the output.
    """
    full_keys = [
        "profiles",
        "profile_rows",
        "rows",
        "all_profiles",
        "analysis_rows",
        "examples_all",
    ]
    for key in full_keys:
        rows = report.get(key)
        if isinstance(rows, list) and rows:
            return rows, key

    # Backward-compatible fallback only. This is not suitable for global metrics.
    top = report.get("top_examples", {})
    if isinstance(top, dict):
        dedup: Dict[str, Dict[str, Any]] = {}
        for examples in top.values():
            if isinstance(examples, list):
                for row in examples:
                    if isinstance(row, dict):
                        cid = profile_id(row)
                        if cid:
                            dedup[cid] = row
        if dedup:
            return list(dedup.values()), "top_examples_fallback_partial"

    raise ValueError(
        "No profile rows found in semantic signal report. Regenerate the semantic "
        "signal reports with a version that stores full profile rows under "
        "`profiles` or `profile_rows`."
    )


def add_ratio_features(row: Dict[str, Any], task: str) -> Dict[str, Any]:
    row = deepcopy(row)
    sig = signal_values(row)
    ratios: Dict[str, float] = {}

    if task == "gender":
        female = sig.get("female_role_signal", 0.0)
        male = sig.get("male_role_signal", 0.0)
        relationship = sig.get("relationship_signal", 0.0)
        family = sig.get("family_signal", 0.0)
        violence = sig.get("violence_against_women_signal", 0.0)
        social = sig.get("emotion_social_issue_signal", 0.0)
        ratios = {
            "female_minus_male_role": female - male,
            "relationship_minus_male_role": relationship - male,
            "family_minus_male_role": family - male,
            "female_context_minus_male_role": family + relationship + female + violence - male,
            "violence_social_combo": violence + social,
        }
    elif task == "occupation":
        creator = sig.get("creator_signal", 0.0)
        performer = sig.get("performer_signal", 0.0)
        sports = sig.get("sports_signal", 0.0)
        politics = sig.get("politics_signal", 0.0)
        fan = sig.get("fan_community_signal", 0.0)
        ratios = {
            "performer_minus_creator": performer - creator,
            "creator_minus_performer": creator - performer,
            "fan_minus_creator": fan - creator,
            "performer_fan_minus_creator": performer + fan - creator,
            "sports_minus_performer": sports - performer,
            "politics_minus_creator": politics - creator,
        }

    row["ratio_features"] = ratios
    return row


def macro_f1(y_true: List[str], y_pred: List[str], labels: List[str]) -> float:
    scores = []
    for lab in labels:
        tp = sum(1 for t, p in zip(y_true, y_pred) if t == lab and p == lab)
        fp = sum(1 for t, p in zip(y_true, y_pred) if t != lab and p == lab)
        fn = sum(1 for t, p in zip(y_true, y_pred) if t == lab and p != lab)
        precision = tp / (tp + fp) if (tp + fp) else 0.0
        recall = tp / (tp + fn) if (tp + fn) else 0.0
        f1 = 2 * precision * recall / (precision + recall) if (precision + recall) else 0.0
        scores.append(f1)
    return sum(scores) / len(scores) if scores else 0.0


def metrics(rows: List[Dict[str, Any]], pred_key: str = "_sim_pred") -> Dict[str, float]:
    y_true = [true_label(r) for r in rows]
    y_pred = [str(r.get(pred_key, pred_label(r))) for r in rows]
    labels = sorted(set(y_true) | set(y_pred))
    correct = sum(1 for t, p in zip(y_true, y_pred) if t == p)
    return {
        "n": len(rows),
        "accuracy": correct / len(rows) if rows else 0.0,
        "macro_f1": macro_f1(y_true, y_pred, labels),
    }


def base_rows(rows: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    out = []
    for row in rows:
        r = deepcopy(row)
        r["_sim_pred"] = pred_label(r)
        out.append(r)
    return out


def rule_applies(row: Dict[str, Any], task: str, rule_name: str) -> bool:
    pred = pred_label(row)
    rf = row.get("ratio_features", {})

    if task == "gender":
        if rule_name == "female_flag_from_male_high_relationship_minus_male_2p0":
            return pred == "male" and rf.get("relationship_minus_male_role", 0.0) >= 2.0
        if rule_name == "female_flag_from_male_high_female_context_7p0":
            return pred == "male" and rf.get("female_context_minus_male_role", 0.0) >= 7.0
        if rule_name == "male_flag_from_female_low_relationship_minus_male_0p8":
            return pred == "female" and rf.get("relationship_minus_male_role", 0.0) <= 0.8

    if task == "occupation":
        if rule_name == "politics_recover_from_creator_high_politics_minus_creator_4p5":
            return pred == "creator" and rf.get("politics_minus_creator", 0.0) >= 4.5
        if rule_name == "politics_recover_from_creator_high_politics_minus_creator_6p0":
            return pred == "creator" and rf.get("politics_minus_creator", 0.0) >= 6.0
        if rule_name == "creator_recover_from_performer_low_performer_advantage":
            return (
                pred == "performer"
                and rf.get("performer_minus_creator", 0.0) <= 0.6
                and rf.get("fan_minus_creator", 0.0) <= 0.0
            )
        if rule_name == "sports_recover_from_performer_high_sports_minus_performer_2p0":
            return pred == "performer" and rf.get("sports_minus_performer", 0.0) >= 2.0

    return False


def new_label_for_rule(task: str, rule_name: str) -> str:
    if task == "gender":
        if rule_name.startswith("female_"):
            return "female"
        if rule_name.startswith("male_"):
            return "male"
    if task == "occupation":
        if rule_name.startswith("politics_"):
            return "politics"
        if rule_name.startswith("creator_"):
            return "creator"
        if rule_name.startswith("sports_"):
            return "sports"
    raise ValueError(f"Unknown rule target for {task}: {rule_name}")


